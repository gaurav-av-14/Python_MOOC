def row_correct(sudoku: list, row_no: int):
    checked=[]
    row = sudoku[row_no]
    for i in range(len(row)):
        if row[i]==0:
            continue
        elif row[i] in checked or row[i] not in range(1,10):
            return False
        else:
            checked.append(row[i])
    return True

